int		ft_count_if(char **tab, int (*f) (char*))
{
	int n;
	int i;
	int j;

	n = 0;
	i = 0;
	j = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			n++;
		i++;
	}
	return (n);
}
